function validateSchema(schemaName, expected) {
  const schema = document.components?.schemas?.[schemaName];
  if (!schema) {
    throw new Error(`Missing components.schemas.${schemaName} in ${openApiPath}`);
  }

  const required = new Set(schema.required ?? []);
  for (const property of expected.required) {
    if (!required.has(property)) {
      throw new Error(`Expected ${schemaName} to require ${property}`);
    }
  }

  const propertyNames = Object.keys(schema.properties ?? {}).sort();
  const expectedProperties = [...expected.required, ...(expected.optional ?? [])].sort();
  if (propertyNames.join(",") !== expectedProperties.join(",")) {
    throw new Error(
      `Expected ${schemaName} properties ${expectedProperties.join(",")}, got ${propertyNames.join(",")}`,
    );
  }
}

function assertQueryParameter(parameters, name, expected) {
  const parameter = parameters.find((candidate) => candidate.name === name && candidate.in === "query");
  if (!parameter) {
    throw new Error(`Expected query parameter ${name}`);
  }
  if (Boolean(parameter.required) !== expected.required) {
    throw new Error(`Expected query parameter ${name} required=${expected.required}`);
  }
  if (parameter.schema?.type !== expected.type) {
    throw new Error(`Expected query parameter ${name} type=${expected.type}, got ${parameter.schema?.type}`);
  }
  if (expected.enumValues) {
    const actual = (parameter.schema?.enum ?? []).join(",");
    const desired = expected.enumValues.join(",");
    if (actual !== desired) {
      throw new Error(`Expected query parameter ${name} enum=${desired}, got ${actual}`);
    }
  }
}
