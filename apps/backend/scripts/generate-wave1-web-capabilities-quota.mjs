import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(scriptDir, "..");
const repoRoot = resolve(appRoot, "../..");

const openApiPath = resolve(repoRoot, "openapi/opentoggl-web.openapi.json");
const targetPath = resolve(appRoot, "internal/http/wave1_web_capabilities_quota.generated.go");

const document = JSON.parse(readFileSync(openApiPath, "utf8"));

const operations = [
  {
    path: "/web/v1/workspaces/{workspace_id}/capabilities",
    method: "get",
    operationId: "get-workspace-capabilities",
    responseSchema: "CapabilitySnapshot",
  },
  {
    path: "/web/v1/workspaces/{workspace_id}/quota",
    method: "get",
    operationId: "get-workspace-quota",
    responseSchema: "QuotaWindow",
    headers: [
      "X-OpenToggl-Quota-Remaining",
      "X-OpenToggl-Quota-Reset-In-Secs",
      "X-OpenToggl-Quota-Total",
    ],
  },
];

for (const operation of operations) {
  const source = document.paths?.[operation.path]?.[operation.method];
  if (!source) {
    throw new Error(`Missing ${operation.method.toUpperCase()} ${operation.path} in ${openApiPath}`);
  }
  if (source.operationId !== operation.operationId) {
    throw new Error(
      `Expected ${operation.method.toUpperCase()} ${operation.path} operationId=${operation.operationId}, got ${source.operationId}`,
    );
  }
  const responseRef = source.responses?.["200"]?.content?.["application/json"]?.schema?.$ref;
  if (responseRef !== `#/components/schemas/${operation.responseSchema}`) {
    throw new Error(
      `Expected ${operation.method.toUpperCase()} ${operation.path} response schema ${operation.responseSchema}, got ${responseRef}`,
    );
  }
  for (const header of operation.headers ?? []) {
    if (!source.responses?.["200"]?.headers?.[header]?.$ref) {
      throw new Error(`Expected ${operation.method.toUpperCase()} ${operation.path} to declare header ${header}`);
    }
  }
}

