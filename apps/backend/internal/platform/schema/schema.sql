-- OpenToggl PostgreSQL desired-state schema.

create table identity_users (
    id bigserial primary key,
    email text not null unique,
    full_name text not null,
    password_hash text not null,
    api_token text not null unique,
    timezone text not null default 'UTC',
    beginning_of_week integer not null default 0,
    country_id bigint not null default 0,
    default_workspace_id bigint not null default 0,
    state text not null,
    preferences_date_format text not null default 'YYYY-MM-DD',
    preferences_time_of_day_format text not null default 'h:mm A',
    preferences_alpha_features jsonb not null default '[]'::jsonb,
    constraint identity_users_state_check check (state in ('active', 'deactivated', 'deleted'))
);

create table identity_sessions (
    id text primary key,
    user_id bigint not null references identity_users (id) on delete cascade,
    created_at timestamptz not null default now()
);

create table identity_job_records (
    id bigint generated by default as identity primary key,
    job_name text not null,
    user_id bigint not null references identity_users (id) on delete cascade,
    created_at timestamptz not null default now()
);

create index identity_job_records_user_id_idx on identity_job_records (user_id);

create table identity_push_services (
    user_id bigint not null references identity_users (id) on delete cascade,
    fcm_registration_token text not null,
    created_at timestamptz not null default now(),
    primary key (user_id, fcm_registration_token)
);

create index identity_push_services_user_id_idx on identity_push_services (user_id);

create table tenant_organizations (
    id bigint generated by default as identity primary key,
    name text not null
);

create table tenant_workspaces (
    id bigint generated by default as identity primary key,
    organization_id bigint not null references tenant_organizations (id) on delete cascade,
    name text not null,
    logo_storage_key text not null default '',
    avatar_storage_key text not null default '',
    default_currency text not null default 'USD',
    default_hourly_rate double precision not null default 0,
    rounding integer not null default 0,
    rounding_minutes integer not null default 0,
    display_policy text not null default 'standard',
    only_admins_may_create_projects boolean not null default false,
    only_admins_may_create_tags boolean not null default false,
    only_admins_see_team_dashboard boolean not null default false,
    projects_billable_by_default boolean not null default false,
    projects_private_by_default boolean not null default false,
    projects_enforce_billable boolean not null default false,
    reports_collapse boolean not null default false,
    public_project_access text not null default 'members',
    report_locked_at text not null default '',
    show_timesheet_view boolean not null default true,
    required_time_entry_fields jsonb not null default '[]'::jsonb,
    constraint tenant_workspaces_display_policy_check check (display_policy in ('standard', 'hide_start_end_times')),
    constraint tenant_workspaces_public_project_access_check check (public_project_access in ('members', 'admins'))
);

create index tenant_workspaces_organization_id_idx on tenant_workspaces (organization_id);

create table billing_accounts (
    organization_id bigint primary key references tenant_organizations (id) on delete cascade,
    customer_id text not null default '',
    subscription_plan text not null,
    subscription_state text not null,
    quota_remaining integer not null default 0,
    quota_resets_in_secs integer not null default 0,
    quota_total integer not null default 0
);

create table web_user_homes (
    user_id bigint primary key references identity_users (id) on delete cascade,
    organization_id bigint not null references tenant_organizations (id) on delete cascade,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade
);

create table membership_workspace_members (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    user_id bigint references identity_users (id) on delete set null,
    email text not null,
    full_name text not null default '',
    role text not null,
    state text not null,
    hourly_rate double precision,
    labor_cost double precision,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint membership_workspace_members_role_check check (role in ('owner', 'admin', 'member')),
    constraint membership_workspace_members_state_check check (state in ('invited', 'joined', 'disabled', 'restored', 'removed'))
);

create index membership_workspace_members_workspace_id_idx on membership_workspace_members (workspace_id);
create unique index membership_workspace_members_workspace_email_key on membership_workspace_members (workspace_id, lower(email));
create unique index membership_workspace_members_workspace_user_key
    on membership_workspace_members (workspace_id, user_id)
    where user_id is not null;

create table catalog_clients (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    name text not null,
    archived boolean not null default false,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now()
);

create index catalog_clients_workspace_id_idx on catalog_clients (workspace_id);
create unique index catalog_clients_workspace_name_key on catalog_clients (workspace_id, lower(name));

create table catalog_projects (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    client_id bigint references catalog_clients (id) on delete set null,
    name text not null,
    active boolean not null default true,
    pinned boolean not null default false,
    template boolean not null default false,
    actual_seconds bigint not null default 0,
    recurring boolean not null default false,
    recurring_period_start date,
    recurring_period_end date,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now()
);

create index catalog_projects_workspace_id_idx on catalog_projects (workspace_id);
create index catalog_projects_client_id_idx on catalog_projects (client_id);
create unique index catalog_projects_workspace_name_key on catalog_projects (workspace_id, lower(name));

create table catalog_project_users (
    project_id bigint not null references catalog_projects (id) on delete cascade,
    user_id bigint not null references identity_users (id) on delete cascade,
    role text not null default 'member',
    created_at timestamptz not null default now(),
    primary key (project_id, user_id),
    constraint catalog_project_users_role_check check (role in ('admin', 'member'))
);

create index catalog_project_users_user_id_idx on catalog_project_users (user_id);

create table catalog_groups (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    name text not null,
    has_users boolean not null default false,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now()
);

create index catalog_groups_workspace_id_idx on catalog_groups (workspace_id);
create unique index catalog_groups_workspace_name_key on catalog_groups (workspace_id, lower(name));

create table catalog_tags (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    name text not null,
    deleted_at timestamptz,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now()
);

create index catalog_tags_workspace_id_idx on catalog_tags (workspace_id);
create unique index catalog_tags_workspace_name_key on catalog_tags (workspace_id, lower(name));

create table catalog_tasks (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    project_id bigint references catalog_projects (id) on delete set null,
    name text not null,
    active boolean not null default true,
    created_by bigint references identity_users (id) on delete set null,
    created_at timestamptz not null default now()
);

create index catalog_tasks_workspace_id_idx on catalog_tasks (workspace_id);
create index catalog_tasks_project_id_idx on catalog_tasks (project_id);
create unique index catalog_tasks_workspace_name_key on catalog_tasks (workspace_id, lower(name));

create table tracking_time_entries (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    user_id bigint not null references identity_users (id) on delete cascade,
    client_id bigint references catalog_clients (id) on delete set null,
    project_id bigint references catalog_projects (id) on delete set null,
    task_id bigint references catalog_tasks (id) on delete set null,
    description text not null default '',
    billable boolean not null default false,
    start_time timestamptz not null,
    stop_time timestamptz,
    duration_seconds integer not null,
    created_with text not null default '',
    tag_ids jsonb not null default '[]'::jsonb,
    expense_ids jsonb not null default '[]'::jsonb,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create index tracking_time_entries_workspace_id_idx on tracking_time_entries (workspace_id);
create index tracking_time_entries_user_id_idx on tracking_time_entries (user_id);
create index tracking_time_entries_project_id_idx on tracking_time_entries (project_id);
create index tracking_time_entries_task_id_idx on tracking_time_entries (task_id);
create index tracking_time_entries_start_time_idx on tracking_time_entries (start_time);

create table tracking_running_timers (
    user_id bigint primary key references identity_users (id) on delete cascade,
    time_entry_id bigint references tracking_time_entries (id) on delete cascade,
    started_at timestamptz not null default now()
);

create table tracking_expenses (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    user_id bigint not null references identity_users (id) on delete cascade,
    time_entry_id bigint references tracking_time_entries (id) on delete set null,
    description text not null default '',
    category text not null default '',
    state text not null default 'draft',
    currency text not null default 'USD',
    total_amount integer not null default 0,
    date_of_expense date not null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    deleted_at timestamptz,
    constraint tracking_expenses_state_check check (state in ('draft', 'submitted', 'approved', 'rejected', 'reimbursed'))
);

create index tracking_expenses_workspace_id_idx on tracking_expenses (workspace_id);
create index tracking_expenses_user_id_idx on tracking_expenses (user_id);
create index tracking_expenses_time_entry_id_idx on tracking_expenses (time_entry_id);

create table tracking_favorites (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    user_id bigint not null references identity_users (id) on delete cascade,
    project_id bigint references catalog_projects (id) on delete set null,
    task_id bigint references catalog_tasks (id) on delete set null,
    description text not null default '',
    billable boolean not null default false,
    public boolean not null default false,
    rank integer not null default 0,
    tag_ids jsonb not null default '[]'::jsonb,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create index tracking_favorites_workspace_id_idx on tracking_favorites (workspace_id);
create index tracking_favorites_user_id_idx on tracking_favorites (user_id);

create table tracking_goals (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    user_id bigint not null references identity_users (id) on delete cascade,
    creator_user_id bigint not null references identity_users (id) on delete cascade,
    name text not null,
    active boolean not null default true,
    billable boolean not null default false,
    comparison text not null default 'gte',
    recurrence text not null default '',
    icon text not null default '',
    target_seconds integer not null default 0,
    start_date date not null,
    end_date date,
    project_ids jsonb not null default '[]'::jsonb,
    task_ids jsonb not null default '[]'::jsonb,
    tag_ids jsonb not null default '[]'::jsonb,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create index tracking_goals_workspace_id_idx on tracking_goals (workspace_id);
create index tracking_goals_user_id_idx on tracking_goals (user_id);

create table tracking_reminders (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    frequency integer not null,
    threshold_hours double precision not null,
    email_reminder_enabled boolean not null default false,
    slack_reminder_enabled boolean not null default false,
    user_ids jsonb not null default '[]'::jsonb,
    group_ids jsonb not null default '[]'::jsonb,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint tracking_reminders_frequency_check check (frequency in (1, 7))
);

create index tracking_reminders_workspace_id_idx on tracking_reminders (workspace_id);

create table tracking_timeline_events (
    id bigint generated by default as identity primary key,
    user_id bigint not null references identity_users (id) on delete cascade,
    desktop_id text not null default '',
    filename text not null default '',
    title text not null default '',
    start_time integer not null,
    end_time integer not null,
    idle boolean not null default false,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create index tracking_timeline_events_user_id_idx on tracking_timeline_events (user_id);
create index tracking_timeline_events_start_time_idx on tracking_timeline_events (start_time);

create table governance_alerts (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    project_id bigint references catalog_projects (id) on delete set null,
    receiver_roles jsonb not null default '[]'::jsonb,
    receiver_users jsonb not null default '[]'::jsonb,
    source_kind text not null default '',
    threshold_type text not null default '',
    thresholds jsonb not null default '[]'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create index governance_alerts_workspace_id_idx on governance_alerts (workspace_id);
create index governance_alerts_project_id_idx on governance_alerts (project_id);

create table governance_timesheet_setups (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    member_user_id bigint not null references identity_users (id) on delete cascade,
    approver_user_id bigint references identity_users (id) on delete set null,
    approver_user_ids jsonb not null default '[]'::jsonb,
    approver_layers jsonb not null default '{}'::jsonb,
    periodicity text not null default 'weekly',
    reminder_day integer not null default 1,
    reminder_time text not null default '17:00',
    email_reminder_enabled boolean not null default false,
    slack_reminder_enabled boolean not null default false,
    start_date date not null,
    end_date date,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint governance_timesheet_setups_periodicity_check check (periodicity in ('daily', 'weekly', 'monthly')),
    constraint governance_timesheet_setups_reminder_day_check check (reminder_day between 0 and 6)
);

create index governance_timesheet_setups_workspace_id_idx on governance_timesheet_setups (workspace_id);
create index governance_timesheet_setups_member_user_id_idx on governance_timesheet_setups (member_user_id);

create table governance_timesheets (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    timesheet_setup_id bigint not null references governance_timesheet_setups (id) on delete cascade,
    member_user_id bigint not null references identity_users (id) on delete cascade,
    start_date date not null,
    end_date date not null,
    status text not null default 'open',
    force_approved boolean not null default false,
    rejection_comment text not null default '',
    review_layer integer not null default 0,
    submitted_at timestamptz,
    approved_or_rejected_at timestamptz,
    approved_or_rejected_id bigint references identity_users (id) on delete set null,
    reminder_sent_at timestamptz,
    timezone text not null default 'UTC',
    reviews jsonb not null default '[]'::jsonb,
    deleted_at timestamptz,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint governance_timesheets_status_check check (status in ('open', 'submitted', 'approved', 'rejected', 'reopened'))
);

create unique index governance_timesheets_setup_start_key on governance_timesheets (timesheet_setup_id, start_date);
create index governance_timesheets_workspace_id_idx on governance_timesheets (workspace_id);
create index governance_timesheets_member_user_id_idx on governance_timesheets (member_user_id);
