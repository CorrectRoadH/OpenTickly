);

create index membership_organization_invitations_org_idx on membership_organization_invitations (organization_id, created_at desc);

create table membership_organization_invitation_workspaces (
    invitation_id bigint not null references membership_organization_invitations (id) on delete cascade,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
