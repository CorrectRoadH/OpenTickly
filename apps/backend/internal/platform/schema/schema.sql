    id bigserial primary key,
    email text not null unique,
    full_name text not null,
    password_hash text not null,
    api_token text not null unique,
    timezone text not null default 'UTC',
    beginning_of_week integer not null default 0,
    country_id bigint not null default 0,
    default_workspace_id bigint not null default 0,
    state text not null,
    send_product_emails boolean not null default true,
    send_weekly_report boolean not null default true,
    tos_accept_needed boolean not null default true,
    product_emails_disable_code text not null,
    weekly_report_disable_code text not null,
    preferences_animation_opt_out boolean not null default false,
    preferences_collapse_time_entries boolean not null default true,
    preferences_date_format text not null default 'YYYY-MM-DD',
    preferences_duration_format text not null default 'improved',
    preferences_hide_sidebar_right boolean not null default false,
    preferences_is_goals_view_shown boolean not null default true,
    preferences_keyboard_shortcuts_enabled boolean not null default true,
    preferences_language_code text not null default 'en-US',
    preferences_manual_entry_mode text not null default 'timer',
    preferences_manual_mode boolean not null default false,
    preferences_project_shortcut_enabled boolean not null default false,
    preferences_reports_collapse boolean not null default false,
    preferences_send_added_to_project_notification boolean not null default true,
    preferences_send_daily_project_invites boolean not null default true,
    preferences_send_product_release_notification boolean not null default true,
    preferences_send_timer_notifications boolean not null default true,
    preferences_show_time_in_title boolean not null default true,
    preferences_tags_shortcut_enabled boolean not null default false,
    preferences_time_of_day_format text not null default 'h:mm A',
    preferences_alpha_features jsonb not null default '[]'::jsonb,
    constraint identity_users_state_check check (state in ('active', 'deactivated', 'deleted'))
