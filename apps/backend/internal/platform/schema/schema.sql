create table catalog_rates (
    id bigint generated by default as identity primary key,
    workspace_id bigint not null references tenant_workspaces (id) on delete cascade,
    type text not null,
    level text not null,
    level_id bigint not null,
    amount double precision not null,
    creator_id bigint references identity_users (id) on delete set null,
    start_at timestamptz not null,
    end_at timestamptz,
    rate_change_mode text not null,
    project_id bigint references catalog_projects (id) on delete cascade,
    project_user_id bigint,
    planned_task_id bigint references catalog_tasks (id) on delete cascade,
    workspace_user_id bigint references membership_workspace_members (id) on delete cascade,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint catalog_rates_type_check check (type in ('billable_rates', 'labor_costs')),
    constraint catalog_rates_level_check check (level in ('workspace', 'workspace_user', 'project', 'project_user', 'task')),
    constraint catalog_rates_amount_positive_check check (amount > 0),
    constraint catalog_rates_change_mode_check check (rate_change_mode in ('override-all', 'override-current', 'start-today'))
);

create index catalog_rates_lookup_idx on catalog_rates (workspace_id, level, level_id, type, start_at desc);
create index catalog_rates_project_id_idx on catalog_rates (project_id);
create index catalog_rates_planned_task_id_idx on catalog_rates (planned_task_id);
create index catalog_rates_workspace_user_id_idx on catalog_rates (workspace_user_id);

