create index identity_push_services_user_id_idx on identity_push_services (user_id);

create table importing_exports (
    id bigint generated by default as identity primary key,
    scope text not null,
    scope_id bigint not null,
    requested_by bigint not null references identity_users (id) on delete cascade,
    token text not null unique,
